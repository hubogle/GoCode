func minArray(numbers []int) int {
	l, r := 0, len(numbers)-1
	for l < r {
		mid := l + ((r - l) >> 1)
		if numbers[mid] < numbers[r] {
			r = mid
		} else if numbers[mid] > numbers[r] {
			l = mid + 1
		} else {
			r--
		} // å­˜åœ¨é‡å¤å€¼
	}
	return numbers[l]
}

