/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func levelOrder(root *TreeNode) [][]int {
	if root == nil {
		return nil
	}
	var ans [][]int
	var queue *list.List
	queue = list.New()
	queue.PushBack(root)
	for queue.Len() > 0 {
		n := queue.Len()
		val := make([]int, 0, n)
		for ; n > 0; n-- {
			node := queue.Remove(queue.Front()).(*TreeNode)
			val = append(val, node.Val)
			if node.Left != nil {
				queue.PushBack(node.Left)
			}
			if node.Right != nil {
				queue.PushBack(node.Right)
			}
		}
		ans = append(ans, val)
	}
	return ans
}

